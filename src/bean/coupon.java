package bean;

public class coupon {

	
	private String id;
	private String coupon;
	private int discount;
	private String buy;
	private int useCK;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCoupon() {
		return coupon;
	}
	public void setCoupon(String coupon) {
		this.coupon = coupon;
	}
	public int getDiscount() {
		return discount;
	}
	public void setDiscount(int discount) {
		this.discount = discount;
	}
	public String getBuy() {
		return buy;
	}
	public void setBuy(String buy) {
		this.buy = buy;
	}
	public int getUseCK() {
		return useCK;
	}
	public void setUseCK(int useCK) {
		this.useCK = useCK;
	}
	
	
	
	
}
